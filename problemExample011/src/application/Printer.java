package application;

public class Printer {

	public static void print(int arg) {
		System.out.println(arg);
	}
	
	public static void print(String arg) {
		System.out.println(arg);
	}

	public static void print(double arg) {
		System.out.println(arg);
	}

	public static void print(char arg) {
		System.out.println(arg);
	}
	
}
